// Package queues 提供了通用的队列数据结构接口。
//
// 该包定义了队列的基本操作接口，支持不同的队列实现。
// 队列遵循先进先出（FIFO）的原则。
package queues

// Queue 定义了通用队列的接口。
//
// Queue 提供了基本的队列操作，支持任意类型的数据存储。
// 实现者需要保证线程安全性（如果在并发环境中使用）。
//
// 基本操作：
//   - Push: 将元素添加到队列尾部
//   - Pop: 从队列头部移除并返回元素
type Queue interface {
	// Push 将元素添加到队列尾部。
	//
	// 参数 m 是要添加的元素，可以是任意类型。
	Push(m any)

	// Pop 从队列头部移除并返回元素。
	//
	// 如果队列为空，返回 nil。
	// 返回队列头部的元素。
	Pop() any
}
